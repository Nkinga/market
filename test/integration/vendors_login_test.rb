require 'test_helper'

class VendorsLoginTest < ActionDispatch::IntegrationTest
  def setup
    @vendor = vendors(:michael)
  end
  
  test "login with invalid information" do
    get login_path
    assert_template 'sessions/new'
    post login_path, params: { session: { email: "", password: "" } }
    assert_template 'sessions/new'
    assert_not flash.empty?
    get root_path
    assert flash.empty?
  end
  
  test "login with valid information followed by logout" do
    get login_path
    post login_path, params: { session: { email: @vendor.email,
                                          password: 'password' } }
    assert_redirected_to @vendor
    follow_redirect!
    assert_template 'vendors/show'
    assert_select "a[href=?]", login_path, count: 0
    assert_select "a[href=?]", logout_path
    assert_select "a[href=?]", vendor_path(@vendor)
    delete logout_path
    assert_not is_logged_in?
    assert_redirected_to root_url
    follow_redirect!
    assert_select "a[href=?]", login_path
    assert_select "a[href=?]", logout_path, count: 0
    assert_select "a[href=?]", vendor_path(@vendor), count: 0
  end
end
